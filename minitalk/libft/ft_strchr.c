#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	if (*str == c || c > 127)
		return ((char *)str);
	if (str == NULL)
		return (NULL);
	while (*str != '\0')
	{
		if (*str == c)
			return ((char *)str);
		str++;
	}
	if (*str == c)
		return ((char *)str);
	return (NULL);
}
